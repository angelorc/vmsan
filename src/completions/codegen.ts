#!/usr/bin/env bun
// Generates shell completion scripts from the commands.ts manifest.
// Run via: bun run src/completions/codegen.ts  (or: bun run build)

import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { CLI_NAME, GLOBAL_FLAGS, SUBCOMMANDS } from "./commands.ts";
import type { FlagDef, PositionalDef, SubcommandDef } from "./commands.ts";

const dir = dirname(fileURLToPath(import.meta.url));
const outDir = join(dir, "generated");
mkdirSync(outDir, { recursive: true });

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function subcmdFlagList(sub: SubcommandDef, subGlobal: FlagDef[]): string[] {
  const flags: string[] = [];
  for (const f of sub.flags) {
    flags.push("--" + f.long);
    if (f.short) flags.push("-" + f.short);
  }
  for (const g of subGlobal) flags.push("--" + g.long);
  return flags;
}

function collectEnumFlags(subcommands: SubcommandDef[]): Map<string, string[]> {
  const map = new Map<string, string[]>();
  for (const sub of subcommands) {
    for (const f of sub.flags) {
      if (f.choices && !map.has(f.long)) map.set(f.long, f.choices);
    }
  }
  return map;
}

// ---------------------------------------------------------------------------
// Bash
// ---------------------------------------------------------------------------

function generateBash(cliName: string, globalFlags: FlagDef[], subcommands: SubcommandDef[]): string {
  const U = cliName.toUpperCase().replace(/-/g, "_");
  const subGlobal = globalFlags.filter((f) => !f.topLevelOnly);
  const subcmdNames = subcommands.map((s) => s.name).join(" ");
  const enumFlags = collectEnumFlags(subcommands);

  let enumBlock = "";
  for (const [long, choices] of enumFlags) {
    enumBlock += "    --" + long + ")\n";
    enumBlock += '      COMPREPLY=($(compgen -W "' + choices.join(" ") + '" -- "$cur"))\n';
    enumBlock += "      return\n";
    enumBlock += "      ;;\n";
  }

  // Positional completion groups
  const variadicVmId = subcommands
    .filter((s) => s.positionals[0]?.completeWith === "vmId" && s.positionals[0]?.variadic)
    .map((s) => s.name);
  const nonVariadicVmId = subcommands
    .filter((s) => s.positionals[0]?.completeWith === "vmId" && !s.positionals[0]?.variadic)
    .map((s) => s.name);
  const shellPos = subcommands
    .filter((s) => s.positionals[0]?.completeWith === "shell")
    .map((s) => s.name);

  let positionalBlock = "";
  if (nonVariadicVmId.length > 0) {
    positionalBlock += "    " + nonVariadicVmId.join("|") + ")\n";
    positionalBlock += '      if [[ $cword -eq 2 && "$cur" != -* ]]; then\n';
    positionalBlock += '        COMPREPLY=($(compgen -W "$vm_ids" -- "$cur"))\n';
    positionalBlock += "        return\n";
    positionalBlock += "      fi\n";
    positionalBlock += "      ;;\n";
  }
  if (variadicVmId.length > 0) {
    positionalBlock += "    " + variadicVmId.join("|") + ")\n";
    positionalBlock += '      if [[ "$cur" != -* ]]; then\n';
    positionalBlock += '        COMPREPLY=($(compgen -W "$vm_ids" -- "$cur"))\n';
    positionalBlock += "        return\n";
    positionalBlock += "      fi\n";
    positionalBlock += "      ;;\n";
  }
  if (shellPos.length > 0) {
    positionalBlock += "    " + shellPos.join("|") + ")\n";
    positionalBlock += "      if [[ $cword -eq 2 ]]; then\n";
    positionalBlock += '        COMPREPLY=($(compgen -W "bash zsh fish powershell" -- "$cur"))\n';
    positionalBlock += "        return\n";
    positionalBlock += "      fi\n";
    positionalBlock += "      ;;\n";
  }

  // Flag completion cases per subcommand
  let flagBlock = "";
  for (const sub of subcommands) {
    const flags = subcmdFlagList(sub, subGlobal);
    flagBlock += "    " + sub.name + ")\n";
    flagBlock += '      COMPREPLY=($(compgen -W "' + flags.join(" ") + '" -- "$cur"))\n';
    flagBlock += "      ;;\n";
  }

  let out = "";
  out += "# " + cliName + " bash completion\n";
  out += "# Add to ~/.bashrc:\n";
  out += "#   eval \"$(" + cliName + " completion bash)\"\n";
  out += "# This file is generated by src/completions/codegen.ts — edit commands.ts to change it.\n";
  out += "\n";
  out += "_" + cliName + "_get_vm_ids() {\n";
  out += "  local " + cliName + '_dir="${' + U + '_DIR:-$HOME/.' + cliName + '}"\n';
  out += "  local vms_dir=\"$" + cliName + '_dir/vms"\n';
  out += '  if [[ -d "$vms_dir" ]]; then\n';
  out += '    for f in "$vms_dir"/*.json; do\n';
  out += '      [[ -f "$f" ]] || continue\n';
  out += '      local base="${f##*/}"\n';
  out += "      printf '%s\\n' \"${base%.json}\"\n";
  out += "    done\n";
  out += "  fi\n";
  out += "}\n";
  out += "\n";
  out += "_" + cliName + "_complete() {\n";
  out += "  local cur prev words cword\n";
  out += "  _init_completion 2>/dev/null || {\n";
  out += "    COMPREPLY=()\n";
  out += '    cur="${COMP_WORDS[COMP_CWORD]}"\n';
  out += '    prev="${COMP_WORDS[COMP_CWORD-1]}"\n';
  out += '    words=("${COMP_WORDS[@]}")\n';
  out += "    cword=$COMP_CWORD\n";
  out += "  }\n";
  out += "\n";
  out += '  local subcommands="' + subcmdNames + '"\n';
  out += "\n";
  out += "  if [[ $cword -eq 1 ]]; then\n";
  out += '    COMPREPLY=($(compgen -W "$subcommands" -- "$cur"))\n';
  out += "    return\n";
  out += "  fi\n";
  out += "\n";
  out += '  local subcmd="${words[1]}"\n';
  out += "\n";
  out += '  case "$prev" in\n';
  out += enumBlock;
  out += "  esac\n";
  out += "\n";
  out += "  local vm_ids\n";
  out += "  vm_ids=$(_" + cliName + "_get_vm_ids)\n";
  out += "\n";
  out += '  case "$subcmd" in\n';
  out += positionalBlock;
  out += "  esac\n";
  out += "\n";
  out += '  case "$subcmd" in\n';
  out += flagBlock;
  out += "  esac\n";
  out += "}\n";
  out += "\n";
  out += "complete -F _" + cliName + "_complete " + cliName + "\n";
  return out;
}

// ---------------------------------------------------------------------------
// Zsh
// ---------------------------------------------------------------------------

function generateZsh(cliName: string, globalFlags: FlagDef[], subcommands: SubcommandDef[]): string {
  const subGlobal = globalFlags.filter((f) => !f.topLevelOnly);
  const topLevel = globalFlags.filter((f) => f.topLevelOnly);

  // Global flags array for zsh _arguments
  let globalFlagsBlock = "";
  for (const f of subGlobal) {
    globalFlagsBlock += "    '--" + f.long + "[" + f.description + "]'\n";
  }
  for (const f of topLevel) {
    globalFlagsBlock += "    '(- :)--" + f.long + "[" + f.description + "]'\n";
  }

  // Subcommand list
  let subcmdList = "";
  for (const sub of subcommands) {
    subcmdList += "        '" + sub.name + ":" + sub.description + "'\n";
  }

  // Per-subcommand _arguments blocks
  function zshFlagSpec(f: FlagDef): string {
    const indent = "            ";
    if (f.short) {
      // Zsh brace expansion: '(-s --long)'{-s,--long}'[desc]'
      const prefix = "'(-" + f.short + " --" + f.long + ")'{-" + f.short + ",--" + f.long + "}'";
      if (f.takesArg) {
        let arg = "=[" + f.description + "]:";
        if (f.choices) arg += f.long + ":(" + f.choices.join(" ") + ")";
        else if (f.completeWith === "file") arg += f.long + ":_files";
        else arg += f.long;
        return indent + prefix + arg + "'";
      }
      return indent + prefix + "[" + f.description + "]'";
    } else {
      // Simple flag: '--long[desc]' or '--long=[desc]:arg'
      if (f.takesArg) {
        let arg = "=[" + f.description + "]:";
        if (f.choices) arg += f.long + ":(" + f.choices.join(" ") + ")";
        else if (f.completeWith === "file") arg += f.long + ":_files";
        else arg += f.long;
        return indent + "'--" + f.long + arg + "'";
      }
      return indent + "'--" + f.long + "[" + f.description + "]'";
    }
  }

  function zshPosSpec(pos: PositionalDef, index: number): string {
    const label = pos.description.toLowerCase().replace(/\s+/g, "-");
    const prefix = pos.variadic ? "*" : String(index);
    if (pos.completeWith === "vmId") return "            '" + prefix + ":" + label + ":_" + cliName + "_vm_ids'";
    if (pos.completeWith === "file") return "            '" + prefix + ":" + label + ":_files'";
    if (pos.completeWith === "shell") return "            '" + prefix + ":" + label + ":(bash zsh fish powershell)'";
    return "            '" + prefix + ":" + label + ":'";
  }

  let subcmdCases = "";
  for (const sub of subcommands) {
    subcmdCases += "        " + sub.name + ")\n";
    subcmdCases += "          _arguments \\\n";
    let posIdx = 1;
    for (const pos of sub.positionals) {
      subcmdCases += zshPosSpec(pos, posIdx) + " \\\n";
      if (!pos.variadic) posIdx++;
    }
    for (const f of sub.flags) {
      subcmdCases += zshFlagSpec(f) + " \\\n";
    }
    subcmdCases += "            $global_flags\n";
    subcmdCases += "          ;;\n";
  }

  let out = "";
  out += "#compdef " + cliName + "\n";
  out += "# " + cliName + " zsh completion\n";
  out += "# Add to ~/.zshrc:\n";
  out += "#   eval \"$(" + cliName + " completion zsh)\"\n";
  out += "# Or save directly to fpath:\n";
  out += "#   " + cliName + ' completion zsh > "${fpath[1]}/_' + cliName + '"\n';
  out += "# This file is generated by src/completions/codegen.ts — edit commands.ts to change it.\n";
  out += "\n";
  out += "_" + cliName + "_vm_ids() {\n";
  out += '  local ' + cliName + '_dir="${' + cliName.toUpperCase().replace(/-/g, "_") + '_DIR:-$HOME/.' + cliName + '}"\n';
  out += "  local vms_dir=\"$" + cliName + '_dir/vms"\n';
  out += "  local -a ids\n";
  out += '  if [[ -d "$vms_dir" ]]; then\n';
  out += "    for f in \"$vms_dir\"/*.json(N); do\n";
  out += '      ids+=("${${f##*/}%.json}")\n';
  out += "    done\n";
  out += "  fi\n";
  out += "  _describe 'VM ID' ids\n";
  out += "}\n";
  out += "\n";
  out += "_" + cliName + "() {\n";
  out += "  local context state state_descr line\n";
  out += "  typeset -A opt_args\n";
  out += "\n";
  out += "  local -a global_flags\n";
  out += "  global_flags=(\n";
  out += globalFlagsBlock;
  out += "  )\n";
  out += "\n";
  out += "  _arguments -C \\\n";
  out += "    $global_flags \\\n";
  out += "    '1:subcommand:->subcommand' \\\n";
  out += "    '*::args:->args'\n";
  out += "\n";
  out += "  case $state in\n";
  out += "    subcommand)\n";
  out += "      local -a subcmds\n";
  out += "      subcmds=(\n";
  out += subcmdList;
  out += "      )\n";
  out += "      _describe 'subcommand' subcmds\n";
  out += "      ;;\n";
  out += "    args)\n";
  out += "      case $line[1] in\n";
  out += subcmdCases;
  out += "      esac\n";
  out += "      ;;\n";
  out += "  esac\n";
  out += "}\n";
  out += "\n";
  out += "compdef _" + cliName + " " + cliName + "\n";
  return out;
}

