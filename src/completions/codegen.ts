function generateFish(cliName: string, globalFlags: FlagDef[], subcommands: SubcommandDef[]): string {
  const subGlobal = globalFlags.filter((f) => !f.topLevelOnly);
  const allSubcmdNames = subcommands.map((s) => s.name).join(" ");
  const U = cliName.toUpperCase().replace(/-/g, "_");

  let out = "";
  out += "# " + cliName + " fish completion\n";
  out += "# Install permanently:\n";
  out += "#   " + cliName + " completion fish > ~/.config/fish/completions/" + cliName + ".fish\n";
  out += "# Or source temporarily in config.fish:\n";
  out += "#   " + cliName + " completion fish | source\n";
  out += "# This file is generated by src/completions/codegen.ts — edit commands.ts to change it.\n";
  out += "\n";
  out += "function __" + cliName + "_vm_ids\n";
  out +=
    '    set -l ' + cliName + '_dir (test -n "$' + U + '_DIR"; and echo "$' + U + '_DIR"; or echo "$HOME/.' + cliName + '")\n';
  out += "    set -l vms_dir \"$" + cliName + '_dir/vms"\n';
  out += "    if test -d \"$vms_dir\"\n";
  out += "        for f in $vms_dir/*.json\n";
  out += "            test -f \"$f\"; or continue\n";
  out += "            string replace -r '.*/([^/]+)\\.json$' '$1' \"$f\"\n";
  out += "        end\n";
  out += "    end\n";
  out += "end\n";
  out += "\n";
  out += "function __" + cliName + "_no_subcommand\n";
  out += "    not __fish_seen_subcommand_from " + allSubcmdNames + "\n";
  out += "end\n";
  out += "\n";
  out += "# Disable file completion globally; re-enabled per subcommand where needed\n";
  out += "complete -c " + cliName + " -f\n";
  out += "\n";
  out += "# Global flags\n";
  for (const f of subGlobal) {
    out += "complete -c " + cliName + " -l " + f.long + " -d '" + f.description + "'\n";
  }
  out += "\n";
  out += "# Subcommands\n";
  for (const sub of subcommands) {
    const pad = " ".repeat(Math.max(1, 10 - sub.name.length));
    out +=
      "complete -c " +
      cliName +
      " -n __" +
      cliName +
      "_no_subcommand -a " +
      sub.name +
      pad +
      "-d '" +
      sub.description +
      "'\n";
  }

  for (const sub of subcommands) {
    const hasFlags = sub.flags.length > 0;
    const hasVmId = sub.positionals.some((p) => p.completeWith === "vmId");
    const hasFilePos = sub.positionals.some((p) => p.completeWith === "file");
    const hasShellPos = sub.positionals.some((p) => p.completeWith === "shell");
    if (!hasFlags && !hasVmId && !hasFilePos && !hasShellPos) continue;

    out += "\n";
    out += "# " + sub.name + "\n";
    const cond = "'__fish_seen_subcommand_from " + sub.name + "'";

    if (hasVmId) {
      out += "complete -c " + cliName + " -n " + cond + " -a '(__" + cliName + "_vm_ids)' -d 'VM ID'\n";
    }

    for (const f of sub.flags) {
      let line = "complete -c " + cliName + " -n " + cond;
      if (f.short) line += " -s " + f.short;
      line += " -l " + f.long;
      line += " -d '" + f.description + "'";
      if (f.takesArg) {
        if (f.choices) {
          line += " -r -a '" + f.choices.join(" ") + "'";
        } else if (f.completeWith === "file") {
          line += " -rF";
        } else {
          line += " -r";
        }
      }
      out += line + "\n";
    }

    if (hasFilePos) {
      out += "complete -c " + cliName + " -n " + cond + " -F\n";
    }
    if (hasShellPos) {
      out +=
        "complete -c " + cliName + " -n " + cond + " -a 'bash zsh fish powershell' -d 'Shell'\n";
    }
  }
  out += "\n";
  return out;
}

// ---------------------------------------------------------------------------
// PowerShell
// ---------------------------------------------------------------------------

function generatePowerShell(
  cliName: string,
  globalFlags: FlagDef[],
  subcommands: SubcommandDef[],
): string {
  const U = cliName.toUpperCase().replace(/-/g, "_");
  const subGlobal = globalFlags.filter((f) => !f.topLevelOnly);
  const subcmdNames = subcommands.map((s) => "'" + s.name + "'").join(",");

  const enumFlags = collectEnumFlags(subcommands);

  // Build enum switch cases
  let enumBlock = "";
  for (const [long, choices] of enumFlags) {
    const choicesStr = choices.map((c) => "'" + c + "'").join(",");
    enumBlock += "        '--" + long + "' {\n";
    enumBlock += "            @(" + choicesStr + ") | Where-Object { $_ -like \"$wordToComplete*\" } | ForEach-Object {\n";
    enumBlock += "                [System.Management.Automation.CompletionResult]::new($_, $_, 'ParameterValue', $_)\n";
    enumBlock += "            }\n";
    enumBlock += "            return\n";
    enumBlock += "        }\n";
  }

  // Positional groups
  const variadicVmId = subcommands
    .filter((s) => s.positionals[0]?.completeWith === "vmId" && s.positionals[0]?.variadic)
    .map((s) => s.name);
  const nonVariadicVmId = subcommands
    .filter((s) => s.positionals[0]?.completeWith === "vmId" && !s.positionals[0]?.variadic)
    .map((s) => s.name);
  const shellPos = subcommands
    .filter((s) => s.positionals[0]?.completeWith === "shell")
    .map((s) => s.name);

  let positionalBlock = "";
  if (nonVariadicVmId.length > 0) {
    const match = nonVariadicVmId.map((n) => "'" + n + "'").join(",");
    positionalBlock += "        { $_ -in @(" + match + ") } {\n";
    positionalBlock += "            if ($positionals -eq 0 -and -not $wordToComplete.StartsWith('-')) {\n";
    positionalBlock += "                $vmIds | Where-Object { $_ -like \"$wordToComplete*\" } | ForEach-Object {\n";
    positionalBlock += "                    [System.Management.Automation.CompletionResult]::new($_, $_, 'ParameterValue', \"VM ID: $_\")\n";
    positionalBlock += "                }\n";
    positionalBlock += "                return\n";
    positionalBlock += "            }\n";
    positionalBlock += "        }\n";
  }
  if (variadicVmId.length > 0) {
    const match = variadicVmId.map((n) => "'" + n + "'").join(",");
    positionalBlock += "        { $_ -in @(" + match + ") } {\n";
    positionalBlock += "            if (-not $wordToComplete.StartsWith('-')) {\n";
    positionalBlock += "                $vmIds | Where-Object { $_ -like \"$wordToComplete*\" } | ForEach-Object {\n";
    positionalBlock += "                    [System.Management.Automation.CompletionResult]::new($_, $_, 'ParameterValue', \"VM ID: $_\")\n";
    positionalBlock += "                }\n";
    positionalBlock += "                return\n";
    positionalBlock += "            }\n";
    positionalBlock += "        }\n";
  }
  if (shellPos.length > 0) {
    const match = shellPos.map((n) => "'" + n + "'").join(",");
    positionalBlock += "        { $_ -in @(" + match + ") } {\n";
    positionalBlock += "            if (-not $wordToComplete.StartsWith('-')) {\n";
    positionalBlock += "                @('bash','zsh','fish','powershell') | Where-Object { $_ -like \"$wordToComplete*\" } | ForEach-Object {\n";
    positionalBlock += "                    [System.Management.Automation.CompletionResult]::new($_, $_, 'ParameterValue', $_)\n";
    positionalBlock += "                }\n";
    positionalBlock += "                return\n";
    positionalBlock += "            }\n";
    positionalBlock += "        }\n";
  }

  // $flagMap
  let flagMapBlock = "";
  for (const sub of subcommands) {
    const flags = subcmdFlagList(sub, subGlobal);
    flagMapBlock += "        '" + sub.name + "' = @(" + flags.map((f) => "'" + f + "'").join(",") + ")\n";
  }

  let out = "";
  out += "# " + cliName + " PowerShell completion\n";
  out += "# Add to your $PROFILE:\n";
  out += "#   Invoke-Expression (& " + cliName + " completion powershell | Out-String)\n";
  out += "# This file is generated by src/completions/codegen.ts — edit commands.ts to change it.\n";
  out += "\n";
  out += "function __" + cliName.replace(/-/g, "") + "_GetVmIds {\n";
  out +=
    "    $vmsanDir = if ($env:" + U + "_DIR) { $env:" + U + "_DIR } else { Join-Path $HOME '." + cliName + "' }\n";
  out += "    $vmsDir = Join-Path $vmsanDir 'vms'\n";
  out += "    if (Test-Path $vmsDir -PathType Container) {\n";
  out += "        Get-ChildItem -Path $vmsDir -Filter '*.json' | ForEach-Object { $_.BaseName }\n";
  out += "    }\n";
  out += "}\n";
  out += "\n";
  out += "Register-ArgumentCompleter -Native -CommandName " + cliName + " -ScriptBlock {\n";
  out += "    param($wordToComplete, $commandAst, $cursorPosition)\n";
  out += "\n";
  out += "    $tokens = @($commandAst.CommandElements | ForEach-Object { $_.ToString() })\n";
  out += "    $subcommands = @(" + subcmdNames + ")\n";
  out += "\n";
  out += "    $subcmd = $null\n";
  out += "    $subcmdIdx = -1\n";
  out += "    for ($i = 1; $i -lt $tokens.Count; $i++) {\n";
  out += "        if ($subcommands -contains $tokens[$i]) {\n";
  out += "            $subcmd = $tokens[$i]\n";
  out += "            $subcmdIdx = $i\n";
  out += "            break\n";
  out += "        }\n";
  out += "    }\n";
  out += "\n";
  out += "    # Complete subcommand name\n";
  out += "    if ($null -eq $subcmd) {\n";
  out += "        $subcommands | Where-Object { $_ -like \"$wordToComplete*\" } | ForEach-Object {\n";
  out += "            [System.Management.Automation.CompletionResult]::new($_, $_, 'ParameterValue', $_)\n";
  out += "        }\n";
  out += "        return\n";
  out += "    }\n";
  out += "\n";
  out += "    # Enum value completions for flags with fixed choices.\n";
  out += "    # When $wordToComplete is empty the current token is not yet in $tokens,\n";
  out += "    # so the preceding flag is at $tokens.Count - 1; when non-empty it is at\n";
  out += "    # $tokens.Count - 2.\n";
  out += "    $prevToken = if ($wordToComplete -eq '') {\n";
  out += "        if ($tokens.Count -ge 1) { $tokens[$tokens.Count - 1] } else { '' }\n";
  out += "    } else {\n";
  out += "        if ($tokens.Count -ge 2) { $tokens[$tokens.Count - 2] } else { '' }\n";
  out += "    }\n";
  out += "    switch ($prevToken) {\n";
  out += enumBlock;
  out += "    }\n";
  out += "\n";
  out += "    $vmIds = @(__" + cliName.replace(/-/g, "") + "_GetVmIds)\n";
  out += "\n";
  out += "    # Count positional (non-flag) tokens after the subcommand (excluding the current word)\n";
  out += "    $positionals = 0\n";
  out += "    for ($i = $subcmdIdx + 1; $i -lt $tokens.Count - 1; $i++) {\n";
  out += "        if (-not $tokens[$i].StartsWith('-')) { $positionals++ }\n";
  out += "    }\n";
  out += "\n";
  out += "    # Subcommand-specific positional completions\n";
  out += "    switch ($subcmd) {\n";
  out += positionalBlock;
  out += "    }\n";
  out += "\n";
  out += "    # Flag completions per subcommand\n";
  out += "    $flagMap = @{\n";
  out += flagMapBlock;
  out += "    }\n";
  out += "\n";
  out += "    if ($flagMap.ContainsKey($subcmd)) {\n";
  out += "        $flagMap[$subcmd] | Where-Object { $_ -like \"$wordToComplete*\" } | ForEach-Object {\n";
  out += "            [System.Management.Automation.CompletionResult]::new($_, $_, 'ParameterName', $_)\n";
  out += "        }\n";
  out += "    }\n";
  out += "}\n";
  return out;
}

